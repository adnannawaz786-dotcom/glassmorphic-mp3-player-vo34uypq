export const formatDuration = (seconds) => {
  const mins = Math.floor(seconds / 60);
  const secs = seconds % 60;
  return `${mins}:${secs.toString().padStart(2, '0')}`;
};

export const getTrackById = (id) => {
  return sampleTracks.find(track => track.id === id);
};

export const getTracksByGenre = (genre) => {
  return sampleTracks.filter(track => track.genre === genre);
};

export const getPlaylistById = (id) => {
  return playlists.find(playlist => playlist.id === id);
};

export const getPlaylistTracks = (playlistId) => {
  const playlist = getPlaylistById(playlistId);
  if (!playlist) return [];
  return playlist.tracks.map(trackId => getTrackById(trackId)).filter(Boolean);
};

export const shuffleArray = (array) => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

export const getRandomTracks = (count = 5) => {
  return shuffleArray(sampleTracks).slice(0, count);
};

export const searchTracks = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return sampleTracks.filter(track => 
    track.title.toLowerCase().includes(lowercaseQuery) ||
    track.artist.toLowerCase().includes(lowercaseQuery) ||
    track.album.toLowerCase().includes(lowercaseQuery) ||
    track.genre.toLowerCase().includes(lowercaseQuery)
  );
};